public class U01P01AND {
	public static void main(String[] args) {
		double area;
		double perimeter;
		double width = 4.5; 
		double height = 7.9; 
		perimeter = 2 * width * height;
		area = width * height; 
		System.out.println("The area of the rectangle is " + area);
		System.out.println("The perimeter of the rectangle is " + perimeter );
	
	}
}
